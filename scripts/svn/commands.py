


class SVN():
    """SVN commands object"""

    @staticmethod
    def update():
        print('SVN Update')

    @staticmethod
    def checkout():
        print('SVN Checkout')

    @staticmethod
    def revert():
        print('SVN Revert')

    @staticmethod
    def cleanup():
        print('SVN Cleanup')

    @staticmethod
    def merge():
        print('SVN Merge')

    @staticmethod
    def delete():
        print('SVN delete')

    @staticmethod
    def add():
        print('SVN add')

    @staticmethod
    def undo_add():
        print('SVN undo add')

    @staticmethod
    def commit():
        print('SVN commit')

    @staticmethod
    def unlock():
        print('SVN unlock')

    @staticmethod
    def show_log():
        print('SVN show log')

    @staticmethod
    def diff():
        print('SVN diff')